// if else
    let a = 10
    if (a > 5) {
        console.log(true)
    } else {
        console.log(false)
    }


// switch statement
    let x = "apple"
    switch (x) {
        case "apple":
            console.log("x is apple")
        break;

        case "banana":
            console.log("x is banana")
        break;
        case "default":
            console.log("x is invalid")
        }
